const EscapeCode = "00";

/** see: https://en.wikipedia.org/wiki/ISO_3166-1_alpha-2 */
export function ExpectIso31661A2CountryCode(data:string,onBadData?:OnException){
    Expect(data.length===2,`Not an ISO3166-1 alpha-2 value. Expected length: 2.`,onBadData);
    Expect(IsAlphabetic(data)||EscapeCode,`Not an ISO3166-1 alpha-2 value. Expected alphabetic values or 00.`,onBadData);
}

export function ExpectAssignedIso31661A2CountryCode(data:string,onBadData?:OnException){
    ExpectIso31661A2CountryCode(data,onBadData);
    Expect(AssignedIso31661A2CountryCodes.includes(data as any),`Not a valid assigned Iso3166A2 country code.`);
}


export function MaybeGetIso31661A2CountryCodeEnglishName(countryCode:string):string|undefined{
    ExpectIso31661A2CountryCode(countryCode);
    return dataMap[countryCode.toUpperCase()]?.englishName;
}

